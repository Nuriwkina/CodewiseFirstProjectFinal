package homeAssignmentNew;

public class Calculator {

    public int add(int a, int b) {
        int sum = a + b;
        return sum;

    }

    public int subtract(int d, int c) {
        int sum = d - c;
        return sum;
    }

    public int multiply(int e, int f) {
        int sum = e * f;
        return sum;
    }

    public int divide(int g, int k) {
        int sum = g / k;
        return sum;
    }

    public boolean isEven1(int i) {
        if (i % 2 == 0) {
            return true;

        } else {
            return false;
        }

    }

    public boolean isEven2(int l) {
        if (l % 2 == 0) {
            return true;

        } else {
            return false;
        }
    }
}
